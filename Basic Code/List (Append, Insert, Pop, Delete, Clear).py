list = ["A", "B", "D", "M", "N", "O"]
print("Add Using Append Method ")
list.append("L")
print(list)

print("Add Using Insert Method For Specific Position ")
list.insert(2, "C")
print(list)

print("Remove Last Element Using Pop Method ")
list.pop()
print(list)

print("Remove Specific Position Using Del Method ")
del list[0]
print(list)

print("Clear All Data Using Clear Method ")
list.clear()
print(list)