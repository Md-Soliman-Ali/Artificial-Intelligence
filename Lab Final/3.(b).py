list = ["1", "2", "3", "4", "5", "6"]
print("Adding Using Append Method ")
list.append("7")
print(list)

print("Adding Using Insert Method For Specific Position(2) ")
list.insert(2, "2.5")
print(list)

print("Removeing Last Element Using Pop Method ")
list.pop()
print(list)

print("Removeing Specific Position (0) Using Del Method ")
del list[0]
print(list)

print("Clearing All Data Using Clear Method ")
list.clear()
print(list)
